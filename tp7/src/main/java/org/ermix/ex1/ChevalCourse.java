package org.ermix.ex1;

public class ChevalCourse {
    private String nom;
    private String couleur;
    private int anneeNaissance;
    private int nombreCourses;

    public String getNom() {
        return nom;
    }

    public String getCouleur() {
        return couleur;
    }

    public int getAnneeNaissance() {
        return anneeNaissance;
    }

    public int getNombreCourses() {
        return nombreCourses;
    }

    public void setNom(String value) {
        nom = value;
    }

    public void setCouleur(String value) {
        couleur = value;
    }

    public void setAnnneeNaissance(int value) {
        anneeNaissance = value;
    }

    public void setNombreCourses(int value) {
        nombreCourses = value;
    }
}
